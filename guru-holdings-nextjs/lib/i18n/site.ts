export const messages: Record<Locale, MessageDictionary> = { en, zh, ja, ko };

export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

export function isLocalizedLocale(value: string): value is LocalizedLocale {
  return localizedLocales.includes(value as LocalizedLocale);
}

export function translate(
  locale: Locale,
  key: MessageKey,
  values: Record<string, string | number> = {},
): string {
  return messages[locale][key].replace(/{{\s*([\w]+)\s*}}/g, (token, name: string) =>
    Object.prototype.hasOwnProperty.call(values, name) ? String(values[name]) : token,
  );
}

const localePrefixPattern = /^\/(?:en|zh|ja|ko)(?=\/|$)/;

export function stripLocalePrefix(pathname: string): string {
  const stripped = pathname.replace(localePrefixPattern, '');
  return stripped || '/';
}

export function localizedPath(locale: Locale, href: string): string {
  if (!href.startsWith('/') || href.startsWith('//')) return href;

  const match = href.match(/^([^?#]*)([?#].*)?$/);
  const pathname = stripLocalePrefix(match?.[1] || '/');
  const suffix = match?.[2] || '';
  const localized = locale === 'en'
    ? pathname
    : `/${locale}${pathname === '/' ? '' : pathname}`;

  return `${localized}${suffix}`;
}
